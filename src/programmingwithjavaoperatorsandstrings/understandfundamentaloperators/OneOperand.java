package programmingwithjavaoperatorsandstrings.understandfundamentaloperators;

public class OneOperand {

	public static void main(String[] args) {
		int counter = 0;
		
		// prefix- and postfix- increment operator
		++counter;
		counter++;

		// prefix- and postfix- decrement operator
		--counter;
		counter--;
		
		

	}

}
